package cdccm.pojo;

public class ChildPOJO {

	private String child_first_name;
	private String child_last_name;
	private String date_of_birth;
	private int age;
	private String parent_first_name;
	private String parent_last_name;
	private String street;
	private String city;
	private int pincode;
	private String phone_number;
	private String email;
	private String care_provider;
	private String activity;
	
	public String getFirst_name() {	return child_first_name; }

	public void setFirst_name(String first_name) { this.child_first_name = first_name;}
	
	public String getLast_name() { return child_last_name;	}

	public void setLast_name(String last_name) { this.child_last_name = last_name; }

	public String getDob() { return date_of_birth; }

	public void setDob(String dob) { this.date_of_birth = dob; }
	
	public int getAge() { return age; }

	public void setAge(int age) { this.age = age; }
	
	public String getParentFirst_name() {	return parent_first_name; }

	public void setParentFirst_name(String first_name) { this.parent_first_name = first_name;}
	
	public String getParentLast_name() { return parent_last_name;	}

	public void setParentLast_name(String last_name) { this.parent_last_name = last_name; }
	
	public String getStreet() { return street; }

	public void setStreet(String dob) { this.street = dob; }
	
	public String getCity() { return city; }

	public void setCity(String dob) { this.city = dob; }
	
	public int getPincode() { return pincode; }

	public void setPincode(int pincode) { this.pincode = pincode; }
	
	public String getPhonenumber() { return phone_number; }

	public void setPhonenumber(String phone_number) { this.phone_number = phone_number; }
	
	public String getEmail() { return email; }

	public void setEmail(String email) { this.email = email; }
	
	public String getCareProvider() { return care_provider; }

	public void setCareProvider(String care_provider) { this.care_provider = care_provider; }
	
	public String getActivity() { return activity; }

	public void setActivity(String activity) { this.activity = activity; }


	
}
